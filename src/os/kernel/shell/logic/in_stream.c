#include <shell/in_stream.h>

#include <stream/bufferedStream.h>
#include <keyboard.h>
//------------------------------------------------------------------------------------------
//				Types
//------------------------------------------------------------------------------------------
bufferedStream_t* shell_in_stream;

//------------------------------------------------------------------------------------------
//				Local Vars
//------------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------------
//				Private Function
//------------------------------------------------------------------------------------------

//------------------------------------------------------------------------------------------
//				Public Function
//------------------------------------------------------------------------------------------
void shell_in_stream_init(void)
{
	shell_in_stream = createBufferedStream();

	kbSetCharacterStream(&shell_in_stream->stream);
}

characterStream_t* shell_in_stream_get(void)
{
	return &shell_in_stream->stream;
}

void shell_in_stream_deinit(void)
{
	delete(&shell_in_stream->stream);
}